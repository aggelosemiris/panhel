function getSubjectIdForChapter(chapterId: string) {
  if (chapterId.startsWith('aepp')) {
    return 'aepp';
  }

  return chapterId.split('-')[0];
}

function getChapterLabel(chapterId: string) {
  const subject = getSubjectById(getSubjectIdForChapter(chapterId));
  const chapter = subject?.chapters.find((item) => item.id === chapterId);

  if (!chapter) {
    return chapterId;
  }

  return `Κεφάλαιο ${chapter.number}: ${chapter.title}`;
}

function buildQuestion(chapterId: string, statement: string, answer: 'Σ' | 'Λ', index: number): TheoryQuizQuestion {
  return {
    id: `${chapterId}-tf-q${index + 1}`,
    subjectId: getSubjectIdForChapter(chapterId),
    chapterId,
    prompt: statement,
    options: TRUE_FALSE_OPTIONS,
    answer: answer === 'Σ' ? 'Σωστό' : 'Λάθος',
    explanation: `${getChapterLabel(chapterId)} · Η σωστή απάντηση είναι ${answer === 'Σ' ? 'Σωστό' : 'Λάθος'}.`,
  };
}

export const TRUE_FALSE_QUIZ_DATA: Record<string, TheoryQuizQuestion[]> = Object.fromEntries(
  TRUE_FALSE_SOURCES.map(({ chapterId, statements, answers }) => [
    chapterId,
    statements.map((statement, index) => buildQuestion(chapterId, statement, answers[index] ?? 'Λ', index)),
  ]),
);

export function getTrueFalseQuestions(chapterId: string) {
  return TRUE_FALSE_QUIZ_DATA[chapterId] ?? [];
}
